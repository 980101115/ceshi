package org.spring.springboot.domain;

public class User {
    private Integer u_id;
    private String u_name;
    private String u_password;
    private String u_email;
    private Integer u_level;
    private String u_sign;
    private String u_address;
    private String u_data;

    public Integer getU_id() {
        return u_id;
    }

    public void setU_id(Integer u_id) {
        this.u_id = u_id;
    }

    public String getU_name() {
        return u_name;
    }

    public void setU_name(String u_name) {
        this.u_name = u_name;
    }

    public String getU_password() {
        return u_password;
    }

    public void setU_password(String u_password) {
        this.u_password = u_password;
    }

    public String getU_email() {
        return u_email;
    }

    public void setU_email(String u_email) {
        this.u_email = u_email;
    }

    public Integer getU_level() {
        return u_level;
    }

    public void setU_level(Integer u_level) {
        this.u_level = u_level;
    }

    public String getU_sign() {
        return u_sign;
    }

    public void setU_sign(String u_sign) {
        this.u_sign = u_sign;
    }

    public String getU_address() {
        return u_address;
    }

    public void setU_address(String u_address) {
        this.u_address = u_address;
    }

    public String getU_data() {
        return u_data;
    }

    public void setU_data(String u_data) {
        this.u_data = u_data;
    }
}
